const struct CRGB TETROMINOE_COLORS[] = { CRGB(0, 255, 255), CRGB(0, 0, 255), CRGB(255, 100, 0), CRGB(255, 255, 0), CRGB(20, 255, 20), CRGB(255, 0, 255), CRGB(255, 0, 0) };


void Tetrominoe::rotateLeft()
{
    rotation--;
    if(rotation < 0)
    {
        rotation = (TETROMINOE_ROTATIONS-1);
    }
}

void Tetrominoe::rotateRight()
{
    rotation++;
    if(rotation >= TETROMINOE_ROTATIONS)
    {
        rotation = 0;
    }
}

void Tetrominoe::render(FastLED_NeoMatrix * ledMatrix)
{
    CRGB color = TETROMINOE_COLORS[(int)type];
    for(int i = 0 ; i < TETROMINOE_SIZE; i++)
    {
        for(int j = 0 ; j < TETROMINOE_SIZE; j++)
        {
            if(SHAPES[(int)type][rotation][i][j])
            {
                ledMatrix->drawPixel(x+i,y+j,color);
            }
        }
    }
}

bool Tetrominoe::getShape(int x, int y)
{
    return SHAPES[(int)type][rotation][x][y];
}


